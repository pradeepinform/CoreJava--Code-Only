package max;

public class AllDataType {
	/*
	   DataType
	   
	   Primitive                            non-primitive
	   1-int   							    1-string
	   2-long								2-array
	   3-Byte 1 								3-class
	   4-short								4-interface
	   5-float
	   6-double
	   7-boolean
	   8-char										
	   9-short							
	                     
	  
	   
	   
	  
	 */
	
	public static void main(String[] args) {
		int a=12;
		System.out.println(a);
		byte l=99;
		System.out.println(l);
		long h =10000000L;
		System.out.println(h);
		
		float f=12.22f;
		System.out.println(f);
		boolean t=true;
		System.out.println(t);
	
	}

}
